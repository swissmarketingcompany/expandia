import os
import re

def translate_file(filepath, translations):
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
    except UnicodeDecodeError:
        print(f"Skipping {filepath} due to encoding error")
        return

    original_content = content

    # Apply regex translations FIRST to capture specific patterns before breaking them with partial replacements
    for pattern, replacement in regex_translations:
        # Use re.DOTALL if needed, but standard doesn't use it.
        # \s matches newlines in python re, so \s+ should cover newlines in text.
        content = re.sub(pattern, replacement, content)

    # Sort keys by length descending to prioritize longer matches
    sorted_keys = sorted(translations.keys(), key=len, reverse=True)

    # Create a single regex pattern that matches any of the keys
    patterns = []
    for key in sorted_keys:
        escaped_key = re.escape(key).replace(r'\ ', r'\s+')
        pattern = escaped_key

        # Check if key starts with word char (using \w which includes [a-zA-Z0-9_])
        if re.match(r'^\w', key):
            pattern = r'\b' + pattern
        # Check if key ends with word char
        if re.match(r'.*\w$', key):
            pattern = pattern + r'\b'

        patterns.append(pattern)

    regex = re.compile('|'.join(f'({p})' for p in patterns))

    # Replacement function to look up the translation for the matched text
    def replace_match(match):
        matched_text = match.group(0)

        # Try to find the exact key in translations (keys are already normalized single spaces usually)
        # But here we match with whitespaces.
        # We need to normalize matched text (replace multiple whitespaces with single space) to look up in dict
        normalized_match = re.sub(r'\s+', ' ', matched_text)

        if normalized_match in translations:
            return translations[normalized_match]

        # Fallback search - finding which key matched
        for key in sorted_keys:
            escaped_key = re.escape(key).replace(r'\ ', r'\s+')
            pattern = escaped_key
            if re.match(r'^\w', key):
                pattern = r'\b' + pattern
            if re.match(r'.*\w$', key):
                pattern = pattern + r'\b'

            if re.fullmatch(pattern, matched_text):
                return translations[key]

        return matched_text

    # Apply translation
    content = regex.sub(replace_match, content)

    if content != original_content:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"Translated {filepath}")

