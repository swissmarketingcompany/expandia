function applyTranslations(content, lang) {
    if (lang === 'en') return content;
    
    const translations = lang === 'tr' ? turkishTranslations :
                         lang === 'de' ? germanTranslations :
                         lang === 'fr' ? frenchTranslations : {};
    
    let translatedContent = content;
    
    // Sort keys by length (longest first) to prevent partial replacements
    const sortedKeys = Object.keys(translations).sort((a, b) => b.length - a.length);
    
    for (const english of sortedKeys) {
        const target = translations[english];
        // Use a more robust regex to match text content, but be careful not to break HTML attributes
        // This is a simplified approach; a real parser would be better but overkill here.
        if (english.includes(' ')) {
            translatedContent = translatedContent.replace(new RegExp(english.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g'), target);
        } else {
            const regex = new RegExp(`\\b${english.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'g');
            translatedContent = translatedContent.replace(regex, target);
        }
    }
    
    return translatedContent;
}

